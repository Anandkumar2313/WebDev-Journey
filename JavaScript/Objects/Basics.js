/**
 *      - Object is an Entity
 * 
 *      - Every Object have two thing
 *        1. Properties/Attributes
 *        2. Behaviour/Method
 * 
 *      - Objects are written as key:value
 *      Example - let person = {firstName:"Anand", lastName:"Kumar", age:20}
 *      -> Here key are : firstName, lastName, age
 *      -> Respective values are : "Anand", "Kumar", 20 for each keyrespectively
 */

// Example : 
/**
 * 
 *                      car(object) {Real-Life Entity}
 *                      /          \
 *                     /            \
 *      Properties/Attributes       Behaviours/Methods
 *      1. name : Honda City        1. start
 *      2. manufacturer : Honda     2. stop
 *      3. fuelCapacity : 40L       3. accelerate
 */