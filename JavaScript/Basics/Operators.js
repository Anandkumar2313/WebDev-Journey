let a = 11; // Assignment Operator
let b = 4;
// Addition Operator
console.log("Addition is : ",a+b);

// Subtraction Operator
console.log("Subtraction is ",a-b);

// Multiplication Operator
console.log("Multiplication is ",a*b);

// Division Operator
console.log("Division is : ",a/b);

// Modulo Operator
console.log("Remainder is : ",a%b);
b = 2;
console.log("Remainder is : ",a%b);

// Power Operator
console.log("Power is ",a**b);  // a**b means a power b
console.log("Power is ",a^b);  // a^b means a bitwise XOR not power

